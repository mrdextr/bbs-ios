import Foundation
import UIKit

extension UIColor {
    
    static var bbsBlue: UIColor {
        return UIColor(named: "bbsDarkGrey")!
    }
    
    static var bbsWhite: UIColor {
        return UIColor(named: "bbsWhite")!
    }
    
    static var bbsDarkGrey: UIColor {
        return UIColor(named: "bbsDarkGrey")!
    }
    
    static var bbsLightGrey: UIColor {
        return UIColor(named: "bbsLightGrey")!
    }
}
